	<a name="cat"></a><h3>вкладка CATEGORY</h3><br>
	<p>На этой вкладке доступны для работы две таблицы. Левая - категории, правая - группы категорий.<br>
	На сайте используется вложенный каталог. Глубина вложения - 1 уровень.<br>
	Принцип построения такой - вначале рисуются группы, дальше в них вкладываются категории. Если категории не принадлежат не одной группе, то они добавляются ниже под группами.<br>
	
	<b>Основные правила:</b><br>
	1) если существует группа - она должна содержать хотя бы одну категорию. Иначе ее надо удалить!<br>
	2) сортировка групп главнее сортировки категорий. Категории сортируются внутри группы.<br>
	3) категория может принадлежать нескольким группам <i>(также как и товар может быть в нескольких категориях.)</i><br>
	4) категория может быть вне группы. (одновременно быть в группе и в не группы - не возможно)<br>
	5) Для целей предотвращения случайностей стоит защита в виде чекбокса. Добавление новой категории и удаление существующих принимаются при активированном чекбоксе.<br>
	
	<br><a href="#top">На верх</a></p><br><br>
	
