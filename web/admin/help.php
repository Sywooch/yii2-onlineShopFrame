	<a name="main"></a><h3>Краткое общее описание</h3><br>
	<p>Админ панель предназначена только для работы с товарами.</p>
	<p>После входа админ панель автоматически открывается на странице просмотра товаров - вкладка GOODS.<br>
	Страница делится на две зоны. Слева - меню каталога. Справа - таблица товаров. В этой вкладке можно перемещаться по категориям и видеть соответствующий список товаров.<br>
	Эта основная навигационная страница для работы с товарами.<br>	
	<i>- в самом начале категорий слева может не быть, потому что их пока нет в Базе Данных</i><br>
	<i>- если справа нет товара - значит в соответствующую категорию еще не добавлен не один товар.</i>
	<br>
	<br>
	<b><i>Порядок работы</i></b> - в начале надо добавить необходимые категории и группы категорий. Следующий шаг - добавление товаров. В процессе работы всегда можно вернуться для корректировки категорий и редактирования ранее добавленных товаров.<br>
	В целях безопасности <b><i>Запись добавленного товара удалить нельзя</i></b>. Для "удаления товара" - 1) можно выключить товар 2) можно убрать его из категорий 3) можно использовать текущую запись для редактирования и таким образом добавив новую информацию удалить старую.<br> 
	<br><a href="#top">На верх</a></p><br><br>
	
	<a name="goods"></a><h3>вкладка GOODS</h3><br>
	<p>Эта основная навигация по товарам, как каталог в обычном интернет магазине. Слева меню каталога. Справа товар.<br>
	<br>
	<b>Меню каталога</b> - состоит из списка категорий, которые были добавлены в БД через <i>вкладку CATEGORY</i>. У каждой категории перед названием стоит ее ID. Это справочная информация. <b><i>ID менять нельзя</i></b><br>
	Самая последняя запись "Вне категорий" - это специальная запись (ее нет в БД). Под этой категорией находятся все товары, которые не причислены не к одной категории. Ее можно рассматривать как архивно-служебную категорию, 
	на сайте ее видно не будет.<br>
	<br>
	<b>Таблица товаров</b> - Она показывает все товары из текущей категории. В ней сразу доступно для редактирования часть атрибутов товара (название, артикул, цена). Для этого, после ввода редактируемого значение в поле надо нажать кнопку <img src="img/upd.png">.<br>
	Кроме этого у каждого товара есть еще три кнопки:<br>
	<img src="img/ed.png"> - вход в редактор товара. Там доступны все атрибуты товара. Кроме этого можно назначать товар к определенным категориям каталога. А так же работать с изображениями. <br>
	<img src="img/copy.png"> - создает новый товар на базе существующего. Это удобно для добавления однотипных товаров. <br>
	<img src="img/on.png"> - включает/выключает товар из каталога на сайте. <br>
