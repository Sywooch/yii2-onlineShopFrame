	<a name="edit"></a><h3>редактор товара</h3><br>
	<p>Чтобы попасть в редактор товара, нужно найти необходимый товар через <i>вкладку GOODS</i> и нажать у него кнопку <img src="img/ed.png"><br> 
	В редакторе доступны все возможные поля  товара для их редактирования. Чтобы принять изменения нужно нажать кнопку <img src="img/done.png"> <br>
	Что бы поместить товар в категорию, нужно находясь в редакторе товара выбрать через чекбоксы в левом меню нужные категории каталога и дождаться пока они станут красными. <br>
	<br>
	<b>работа с фото</b><br>
	У одного товара может быть несколько изображений. <br>
	Для каждого изображения нужно "зарезервировать полку", изначально у нового товара нет зарезервированных полок. Поэтому первым делом перед добавлением фото, надо нажать большой зеленый плюс, для добавления полки. В будущем мы поставим туда нужное фото.
	После добавления полки, нужно загрузить фото во временную память, из которой будем создавать превью и сохранять готовое фото в нужную полку. В будущем если нужно, то изображение на полке можно перезаписывать другим изображением. Или можно удалить лишнею полку.<br>
	<br>
	ШАГИ ПО РАБОТЕ С ФОТО:<br>
	1 - если полки нет, то добавляем. Можно сразу несколько.<br>
    2 - жмем выбрать файл - откроется диалоговое окно винды для выбора файла.<br>
	3 - жмем <img src="img/upd.png">.<br>
	после 3 шага должна загрузится картинка во временной памяти.<br>
	4 - мышкой выбираем область на картинке из которой создастся превью картинки в каталоге.<br>
	5 - после выбора области для превью жмем кнопку <button><img src="img/upd.png">принять картинку</button><br>
	после 5 шага включаем полку! Если полок несколько повторяем шаги 2-5 и включаем нужную полку.<br>
	6 - включаем нужную полку. Жмем лампочку. (Включаем/делаем главной - если одна полка, то включаем, если две и более, то делаем главной.)<br>
	
